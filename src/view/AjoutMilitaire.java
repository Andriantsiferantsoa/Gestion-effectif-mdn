public void controleCin(){
    String champ = new String();
        champ = txtCIN.getText();
        int lng = champ.length();
        if (txtCIN.getText().matches("[0-9]*")) {
            if (lng > 11) {
                champ = champ.substring(0, lng - 1);
            }
            txtCIN.setText(champ);
        } else {
            champ = champ.substring(0, lng - 1);
            txtCIN.setText(champ);
        }
}
