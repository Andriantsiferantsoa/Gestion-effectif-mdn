private void Afficher(){
        try{
            DefaultComboBoxModel modelCodeDist = new DefaultComboBoxModel();
            String req = "SELECT * from grade";
            rs = gm.execution(con, req);
            while(rs.next()){
                String codeType = rs.getString("LibGrade");
                
                modelCodeDist.addElement(codeType);
                txtGrade.setModel(modelCodeDist);
            }            
        }
        
        catch(Exception ex){
            System.out.println(ex.toString());
        }
    }

